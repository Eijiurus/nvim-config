-- ============================================================================
-- Yazi 配置文件
-- 使用 yazi.nvim 插件在 Neovim 中集成 Yazi 文件管理器
-- GitHub: mikavilpas/yazi.nvim
-- ============================================================================
--
-- 什么是 Yazi？
-- Yazi 是一个用 Rust 编写的现代终端文件管理器，特点是：
-- - 速度极快
-- - 支持图片预览
-- - 支持多标签页
-- - 高度可配置
--
-- 工作流程示意：
-- ┌─────────────────────────────────────────────────────────────────────────┐
-- │ Neovim 编辑器                                                           │
-- │                                                                         │
-- │  按 R 键                                                                │
-- │     │                                                                   │
-- │     ▼                                                                   │
-- │  ┌───────────────────────────────────────────────────────────────────┐  │
-- │  │ Yazi 文件管理器（浮动窗口）                                        │  │
-- │  │                                                                   │  │
-- │  │  📁 src/                                                          │  │
-- │  │  ├── 📄 main.lua                                                  │  │
-- │  │  ├── 📄 utils.lua                                                 │  │
-- │  │  └── 📁 plugins/                                                  │  │
-- │  │       ├── 📄 editor.lua                                           │  │
-- │  │       └── 📄 yazi.lua  ← 当前文件                                  │  │
-- │  │                                                                   │  │
-- │  │  选择文件后按 Enter，在 Neovim 中打开                              │  │
-- │  └───────────────────────────────────────────────────────────────────┘  │
-- │                                                                         │
-- └─────────────────────────────────────────────────────────────────────────┘
--
-- ============================================================================

return {
	-- 插件地址
	"mikavilpas/yazi.nvim",

	-- ========================================================================
	-- 加载时机
	-- ========================================================================
	-- "VeryLazy": 在 Neovim 启动完成后、空闲时加载
	-- 这样不会影响启动速度，但在需要时已经准备好了
	event = "VeryLazy",

	keys = {
		{
			"R",
			"<cmd>Yazi<cr>",
			desc = "Open yazi at the current file",
		},
	},

	opts = {
		-- ====================================================================
		-- 浮动窗口设置
		-- ====================================================================

		-- 浮动窗口缩放比例
		-- 1 = 100% 屏幕大小（全屏）
		-- 0.9 = 90% 屏幕大小
		-- 0.8 = 80% 屏幕大小
		floating_window_scaling_factor = 1,

		-- 浮动窗口边框样式
		-- "none"    : 无边框
		-- "single"  : 单线边框
		-- "double"  : 双线边框
		-- "rounded" : 圆角边框
		-- "solid"   : 实心边框
		-- "shadow"  : 阴影边框
		yazi_floating_window_border = "none",

		-- ====================================================================
		-- 行为设置
		-- ====================================================================

		-- 打开目录时是否自动启动 Yazi
		-- true  : 当用 nvim 打开目录时（如 nvim .），自动用 Yazi 显示
		-- false : 使用 Neovim 默认的 netrw 或其他文件浏览器
		open_for_directories = true,

		-- 是否支持在多个标签页中打开文件
		-- true  : 可以在 Yazi 中选择多个文件，分别在不同标签页打开
		-- false : 只能打开单个文件
		open_multiple_tabs = true,

		-- ====================================================================
		-- Yazi 内部快捷键映射
		-- ====================================================================
		-- 这些快捷键在 Yazi 浮动窗口内生效
		keymaps = {
			-- F1 : 显示帮助
			show_help = '<f1>',

			-- Ctrl+v : 在垂直分割窗口中打开文件
			-- ┌──────────┬──────────┐
			-- │  原窗口  │  新文件  │
			-- └──────────┴──────────┘
			open_file_in_vertical_split = '<c-v>',

			-- Ctrl+x : 在水平分割窗口中打开文件
			-- ┌──────────────────────┐
			-- │       原窗口         │
			-- ├──────────────────────┤
			-- │       新文件         │
			-- └──────────────────────┘
			open_file_in_horizontal_split = '<c-x>',

			-- Ctrl+t : 在新标签页中打开文件
			open_file_in_tab = '<c-t>',

			-- Ctrl+f : 在选中的目录中进行文本搜索（grep）
			-- 会调用 fzf 或 telescope 进行搜索
			grep_in_directory = '<c-f>',

			-- Ctrl+r : 在选中的目录中进行查找替换
			replace_in_directory = '<c-r>',

			-- Tab : 在已打开的 Buffer 之间循环切换
			cycle_open_buffers = '<tab>',

			-- Ctrl+y : 复制选中文件的相对路径
			-- 例如：src/plugins/yazi.lua
			copy_relative_path_to_selected_files = '<c-y>',

			-- Ctrl+q : 将选中的文件发送到 quickfix 列表
			-- 可以批量选择文件，然后在 quickfix 中逐个处理
			send_to_quickfix_list = '<c-q>',
		},
	},
}

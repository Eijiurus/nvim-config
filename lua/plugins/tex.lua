-- ============================================================================
-- LaTeX 配置文件
-- 使用 vimtex 插件提供 LaTeX 编辑和编译支持
-- GitHub: lervag/vimtex
-- ============================================================================
--
-- 功能说明：
-- vimtex 是 Neovim/Vim 中最强大的 LaTeX 插件，提供：
-- - 语法高亮
-- - 自动编译
-- - PDF 预览同步
-- - 目录导航
-- - 补全支持
--
-- 工作流程：
-- ┌─────────────────┐      ┌─────────────────┐      ┌─────────────────┐
-- │  Neovim 编辑    │ ───► │  LaTeX 编译器   │ ───► │  PDF 阅读器     │
-- │  .tex 文件      │      │  (latexmk)      │      │  (Skim)         │
-- └─────────────────┘      └─────────────────┘      └─────────────────┘
--
-- ============================================================================

return {
	{
		-- 插件地址
		"lervag/vimtex",

		-- ====================================================================
		-- 初始化函数
		-- ====================================================================
		-- init 与 config 的区别：
		-- - init  : 在插件加载前执行（用于设置 vim.g 全局变量）
		-- - config: 在插件加载后执行
		-- vimtex 需要在加载前设置选项，所以使用 init
		init = function()
			-- ================================================================
			-- PDF 查看器设置
			-- ================================================================
			-- 设置用于查看 PDF 的应用程序
			-- 可选值：
			--   "skim"    : macOS 上的 Skim 阅读器（支持正反向搜索）
			--   "zathura" : Linux 上常用的轻量级 PDF 阅读器
			--   "evince"  : GNOME 默认 PDF 阅读器
			--   "okular"  : KDE 默认 PDF 阅读器
			--   "sioyek"  : 专为学术设计的 PDF 阅读器
			--   "general" : 使用系统默认 PDF 阅读器
			vim.g.vimtex_view_method = "zathura"

			-- ================================================================
			-- 快捷键映射设置
			-- ================================================================
			-- 是否启用 vimtex 默认的快捷键映射
			-- 0 : 禁用所有默认映射（你想自定义快捷键时使用）
			-- 1 : 启用默认映射（默认值）
			--
			-- 禁用后，vimtex 的默认快捷键（如 \ll 编译）将不可用
			-- 需要自己手动设置快捷键
			vim.g.vimtex_mappings_enabled = 0

			-- ================================================================
			-- Quickfix 警告过滤
			-- ================================================================
			-- 编译 LaTeX 时忽略特定的警告信息
			-- 这些警告通常是无害的，过滤掉可以让 quickfix 列表更清晰
			vim.g.vimtex_quickfix_ignore_filters = {
				-- 字体相关警告
				-- 当使用的字体没有某些特定样式时会出现
				'Font shape',

				-- "badness 10000" 警告
				-- 表示文本排版有困难（如断行不理想），通常可以忽略
				"badness 10000",

				-- hyperref 包的警告
				-- 处理超链接时的常见警告，通常无害
				"Package hyperref Warning",
			}
		end
	},
}

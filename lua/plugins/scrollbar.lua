-- ============================================================================
-- Scrollbar 配置文件
-- 使用 nvim-scrollbar 插件在编辑器右侧显示滚动条
-- GitHub: petertriho/nvim-scrollbar
-- ============================================================================
--
-- 功能说明：
-- 在窗口右侧显示一个可视化的滚动条，并在滚动条上标记：
-- - 搜索结果位置
-- - Git 修改位置
-- - LSP 诊断位置（错误、警告等）
--
-- 效果示意：
-- ┌────────────────────────────────────────────────────────────────┬──┐
-- │ local function hello()                                        │░░│ ← 当前可视区域（handle）
-- │     print("hello world")                                      │░░│
-- │ end                                                           │  │
-- │                                                               │▪ │ ← 搜索结果标记（黄色）
-- │ -- 这里有一些代码                                              │  │
-- │                                                               │▪ │ ← Git 修改标记（橙色）
-- │ local x = 1                                                   │  │
-- │                                                               │▪ │ ← LSP 错误标记（红色）
-- │                                                               │  │
-- │ -- 更多代码...                                                 │  │
-- └────────────────────────────────────────────────────────────────┴──┘
--                                                                   ↑
--                                                              滚动条
-- ============================================================================

return {
	-- 插件地址
	"petertriho/nvim-scrollbar",

	-- ========================================================================
	-- 依赖插件
	-- ========================================================================
	dependencies = {
		-- nvim-hlslens: 搜索结果增强显示
		-- 提供搜索匹配计数和当前匹配位置信息
		-- 滚动条需要这个插件来显示搜索结果的位置
		"kevinhwang91/nvim-hlslens",
	},

	-- ========================================================================
	-- 插件配置函数
	-- ========================================================================
	config = function()
		-- ====================================================================
		-- Git 标记颜色自定义
		-- ====================================================================
		--
		-- 创建自动命令组，用于设置 Git 相关的滚动条颜色
		-- 使用 augroup 可以方便地管理和清除相关的自动命令
		local group = vim.api.nvim_create_augroup("scrollbar_set_git_colors", {})

		-- 每次进入 Buffer 时设置 Git 标记的颜色
		-- 这样可以确保颜色在不同 colorscheme 下保持一致
		vim.api.nvim_create_autocmd("BufEnter", {
			pattern = "*",           -- 匹配所有文件
			callback = function()
				vim.cmd([[
" Git 新增内容的颜色（绿色系）
" ScrollbarGitAdd       : 滚动条上新增标记的颜色
" ScrollbarGitAddHandle : 当 handle 经过新增标记时的颜色
hi! ScrollbarGitAdd guifg=#8CC85F
hi! ScrollbarGitAddHandle guifg=#A0CF5D

" Git 修改内容的颜色（黄色/橙色系）
" ScrollbarGitChange       : 滚动条上修改标记的颜色
" ScrollbarGitChangeHandle : 当 handle 经过修改标记时的颜色
hi! ScrollbarGitChange guifg=#E6B450
hi! ScrollbarGitChangeHandle guifg=#F0C454

" Git 删除内容的颜色（红色系）
" ScrollbarGitDelete       : 滚动条上删除标记的颜色
" ScrollbarGitDeleteHandle : 当 handle 经过删除标记时的颜色
hi! ScrollbarGitDelete guifg=#F87070
hi! ScrollbarGitDeleteHandle guifg=#FF7B7B ]])
			end,
			group = group,
		})

		-- ====================================================================
		-- 搜索处理器设置
		-- ====================================================================
		-- 集成 nvim-hlslens，在滚动条上显示搜索结果的位置
		-- 当你搜索文本时（/pattern），滚动条上会显示所有匹配项的位置
		require("scrollbar.handlers.search").setup({})

		-- ====================================================================
		-- Gitsigns 处理器设置
		-- ====================================================================
		-- 集成 gitsigns.nvim，在滚动条上显示 Git 修改的位置
		-- 需要安装 gitsigns.nvim 插件
		require("scrollbar.handlers.gitsigns").setup()

		-- ====================================================================
		-- 滚动条主配置
		-- ====================================================================
		require("scrollbar").setup({
			-- ================================================================
			-- 基本设置
			-- ================================================================
			-- 是否显示滚动条
			show = true,

			-- ================================================================
			-- Handle（拖动手柄）设置
			-- ================================================================
			-- Handle 表示当前可视区域在整个文件中的位置
			handle = {
				-- 手柄显示的字符
				-- " " : 使用空格（通过背景色显示）
				-- "█" : 实心块
				-- "▐" : 右半块
				text = " ",

				-- 手柄的颜色（灰褐色）
				color = "#928374",

				-- 当整个文件都在可视范围内时，是否隐藏手柄
				-- true  : 文件很短时隐藏滚动条
				-- false : 始终显示
				hide_if_all_visible = true,
			},

			-- ================================================================
			-- 标记设置
			-- ================================================================
			-- 不同类型标记的颜色
			marks = {
				-- 搜索结果标记颜色（黄色）
				Search = { color = "yellow" },

				-- 其他杂项标记颜色（紫色）
				Misc = { color = "purple" },
			},

			-- ================================================================
			-- 处理器设置
			-- ================================================================
			-- 控制哪些信息显示在滚动条上
			handlers = {
				-- 光标位置标记
				-- false : 不在滚动条上显示当前光标位置
				-- true  : 显示光标位置
				cursor = false,

				-- LSP 诊断信息
				-- true : 在滚动条上显示错误、警告等诊断信息的位置
				diagnostic = true,

				-- Git 修改信息
				-- true : 在滚动条上显示 Git 新增、修改、删除的位置
				gitsigns = true,

				-- 手柄（当前可视区域）
				-- true : 显示当前可视区域的位置
				handle = true,

				-- 搜索结果
				-- true : 在滚动条上显示所有搜索匹配项的位置
				search = true,
			},
		})
	end,
}

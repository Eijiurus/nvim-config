-- ============================================================================
-- Git 配置文件
-- 集成 Git 版本控制相关插件
-- 包含：gitsigns.nvim（Git 状态标记）和 lazygit.nvim（Git GUI）
-- ============================================================================
--
-- 整体功能概览：
-- ┌─────────────────────────────────────────────────────────────────────────┐
-- │                           Git 工作流                                     │
-- ├─────────────────────────────────────────────────────────────────────────┤
-- │                                                                         │
-- │   gitsigns.nvim                      lazygit.nvim                       │
-- │   ┌─────────────────────┐            ┌─────────────────────┐            │
-- │   │ 在编辑器内显示：    │            │ 完整的 Git GUI：    │            │
-- │   │ • 新增行标记（绿色）│            │ • 提交更改          │            │
-- │   │ • 修改行标记（蓝色）│   Ctrl+g   │ • 分支管理          │            │
-- │   │ • 删除行标记（红色）│  ────────► │ • 查看历史          │            │
-- │   │ • Blame 信息       │            │ • 解决冲突          │            │
-- │   │ • Hunk 预览/撤销   │            │ • 推送/拉取         │            │
-- │   └─────────────────────┘            └─────────────────────┘            │
-- │                                                                         │
-- └─────────────────────────────────────────────────────────────────────────┘
--
-- ============================================================================

return {
	-- ========================================================================
	-- 插件 1: gitsigns.nvim
	-- GitHub: lewis6991/gitsigns.nvim
	-- 功能：在编辑器左侧显示 Git 状态标记（新增、修改、删除）
	-- ========================================================================
	--
	-- 效果示意：
	-- ┌──┬─────────────────────────────────────────┐
	-- │  │ 1 │ local M = {}                        │  ← 未修改（无标记）
	-- │▎ │ 2 │ local x = 1  -- 新增的行            │  ← 新增标记（绿色竖线）
	-- │▎ │ 3 │ local y = 2  -- 新增的行            │  ← 新增标记
	-- │░ │ 4 │ local z = 100  -- 修改过的行        │  ← 修改标记（蓝色块）
	-- │_ │ 5 │ -- 下面曾经有一行被删除             │  ← 删除标记（下划线）
	-- │  │ 6 │ return M                            │  ← 未修改
	-- └──┴─────────────────────────────────────────┘
	--  ↑
	-- 标记列
	--
	{
		"lewis6991/gitsigns.nvim",
		config = function()
			require('gitsigns').setup({
				-- ============================================================
				-- 标记符号配置
				-- ============================================================
				-- 定义不同 Git 状态在标记列中显示的符号
				signs = {
					-- 新增的行（之前不存在，现在添加的）
					-- text: 显示的符号（细竖线 ▎）
					add = { text = '▎' },

					-- 修改的行（内容发生变化）
					-- text: 显示的符号（浅色块 ░）
					change = { text = '░' },

					-- 删除的行（在当前行下方有内容被删除）
					-- text: 显示的符号（下划线 _）
					delete = { text = '_' },

					-- 顶部删除（在当前行上方有内容被删除）
					-- text: 显示的符号（上划线 ▔）
					topdelete = { text = '▔' },

					-- 修改并删除（既有修改又有删除）
					-- text: 显示的符号（中等密度块 ▒）
					changedelete = { text = '▒' },

					-- 未跟踪的文件（新文件，尚未 git add）
					-- text: 显示的符号（虚线 ┆）
					untracked = { text = '┆' },
				},
			})

			-- ================================================================
			-- 快捷键设置
			-- ================================================================

			-- <Leader>g- : 跳转到上一个 hunk（修改块）
			-- 用于在多个修改之间快速导航
			vim.keymap.set("n", "<leader>g-", ":Gitsigns prev_hunk<CR>",
				{ noremap = true, silent = true })

			-- <Leader>g= : 跳转到下一个 hunk（修改块）
			vim.keymap.set("n", "<leader>g=", ":Gitsigns next_hunk<CR>",
				{ noremap = true, silent = true })

			-- <Leader>gb : 显示当前行的 Git blame 信息
			-- 显示这行代码是谁在什么时候提交的
			-- 示例输出：John Doe • 3 days ago • feat: add new feature
			vim.keymap.set("n", "<leader>gb", ":Gitsigns blame_line<CR>",
				{ noremap = true, silent = true })

			-- <Leader>gr : 重置当前 hunk（撤销修改）
			-- 将当前修改块恢复到 Git HEAD 的状态
			-- ⚠️ 警告：此操作不可撤销！
			vim.keymap.set("n", "<leader>gr", ":Gitsigns reset_hunk<CR>",
				{ noremap = true, silent = true })

			-- H : 预览当前 hunk 的具体修改内容
			-- 会弹出一个浮动窗口显示 diff
			vim.keymap.set("n", "H", ":Gitsigns preview_hunk<CR>",
				{ noremap = true, silent = true })
		end
	},

	-- ========================================================================
	-- 插件 2: lazygit.nvim
	-- GitHub: kdheepak/lazygit.nvim
	-- 功能：在 Neovim 中打开 lazygit（终端 Git GUI）
	-- ========================================================================
	--
	-- LazyGit 是什么？
	-- 一个功能强大的终端 Git 客户端，提供：
	-- - 可视化的提交历史
	-- - 交互式暂存（stage）
	-- - 分支管理
	-- - 冲突解决
	-- - 交互式 rebase
	--
	-- 效果示意（按 Ctrl+g 打开）：
	-- ┌─────────────────────────────────────────────────────────────────────┐
	-- │ ┌─ Status ──────┬─ Staged ──────┬─ Commits ─────────────────────┐  │
	-- │ │               │               │                               │  │
	-- │ │ M file1.lua   │ (empty)       │ abc1234 feat: add feature     │  │
	-- │ │ M file2.lua   │               │ def5678 fix: bug fix          │  │
	-- │ │ A file3.lua   │               │ ghi9012 docs: update readme   │  │
	-- │ │               │               │                               │  │
	-- │ ├───────────────┴───────────────┴───────────────────────────────┤  │
	-- │ │                        Diff Preview                           │  │
	-- │ │  - local x = 1                                                │  │
	-- │ │  + local x = 100                                              │  │
	-- │ └───────────────────────────────────────────────────────────────┘  │
	-- │                                                                     │
	-- │  [Space] Stage  [c] Commit  [p] Push  [P] Pull  [b] Branches  [?]  │
	-- └─────────────────────────────────────────────────────────────────────┘
	--
	{
		"kdheepak/lazygit.nvim",

		-- 懒加载：只有按下 Ctrl+g 时才加载此插件
		keys = { "<c-g>" },

		config = function()
			-- ============================================================
			-- LazyGit 窗口配置
			-- ============================================================

			-- 浮动窗口缩放比例
			-- 1.0 = 100% 全屏
			-- 0.9 = 90% 屏幕大小
			vim.g.lazygit_floating_window_scaling_factor = 1.0

			-- 窗口透明度
			-- 0   = 完全不透明
			-- 100 = 完全透明
			vim.g.lazygit_floating_window_winblend = 0

			-- 是否使用 neovim-remote
			-- 允许从 lazygit 中打开文件回到 Neovim
			-- 需要安装 neovim-remote: pip install neovim-remote
			vim.g.lazygit_use_neovim_remote = true

			-- ============================================================
			-- 快捷键设置
			-- ============================================================

			-- Ctrl+g : 打开 LazyGit
			vim.keymap.set("n", "<c-g>", ":LazyGit<CR>",
				{ noremap = true, silent = true })
		end
	},


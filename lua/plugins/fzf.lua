-- ============================================================================
-- fzf-lua 配置文件
-- 使用 fzf-lua 插件提供模糊搜索功能
-- GitHub: ibhagwan/fzf-lua
-- ============================================================================
--
-- fzf-lua vs Telescope：
-- ┌─────────────────┬─────────────────┬─────────────────┐
-- │     特性        │    fzf-lua      │   Telescope     │
-- ├─────────────────┼─────────────────┼─────────────────┤
-- │ 搜索引擎        │ fzf (更快)      │ Lua 原生        │
-- │ 启动速度        │ 更快            │ 稍慢            │
-- │ 扩展性          │ 较少            │ 丰富            │
-- │ 依赖            │ 需要 fzf        │ 纯 Lua          │
-- └─────────────────┴─────────────────┴─────────────────┘
--
-- 你的配置同时使用了两者：
-- - Telescope: 文件搜索、Buffer 切换、命令搜索等
-- - fzf-lua  : 全局文本搜索（grep），因为 fzf 的 grep 性能更好
--
-- ============================================================================

-- 快捷键通用选项
local m = { noremap = true }

return {
	-- 插件地址
	"ibhagwan/fzf-lua",

	-- 依赖插件：文件图标支持
	dependencies = { "nvim-tree/nvim-web-devicons" },

	-- ========================================================================
	-- 懒加载设置
	-- ========================================================================
	-- 只有按下 Ctrl+f 时才加载此插件
	-- 这样可以加快 Neovim 启动速度
	keys = { "<c-f>" },

	-- ========================================================================
	-- 插件配置函数
	-- ========================================================================
	config = function()
		local fzf = require('fzf-lua')

		-- ====================================================================
		-- 快捷键映射
		-- ====================================================================

		-- Ctrl+f（普通模式）: 全局文本搜索
		-- 在整个项目中搜索文本内容
		vim.keymap.set('n', '<c-f>', function()
			-- fzf.live_grep_resume({ multiprocess = true, debug = true })
			-- grep: 全局搜索
			-- search = "": 初始搜索内容为空（手动输入）
			-- --layout = default: fzf 窗口布局（搜索框在下方）
			fzf.grep({ search = "", fzf_opts = { ['--layout'] = 'default' } })
		end, m)

		-- Ctrl+f（可视模式）: 搜索选中的文本
		-- 先选中一些文本，然后按 Ctrl+f 搜索它
		vim.keymap.set('x', '<c-f>', function()
			-- grep_visual: 自动获取选中的文本作为搜索内容
			fzf.grep_visual({ fzf_opts = { ['--layout'] = 'default' } })
		end, m)

		-- ====================================================================
		-- fzf-lua 主配置
		-- ====================================================================
		fzf.setup({
			-- ================================================================
			-- 全局恢复设置
			-- ================================================================
			-- 是否启用全局恢复功能
			-- true: 可以通过 fzf.resume() 恢复上次搜索
			global_resume = true,

			-- 恢复时是否同时恢复搜索关键词
			global_resume_query = true,

			-- ================================================================
			-- 窗口选项
			-- ================================================================
			winopts = {
				-- 窗口高度（1 = 100% 屏幕高度）
				height = 1,

				-- 窗口宽度（1 = 100% 屏幕宽度）
				width = 1,

				-- 预览窗口设置
				preview = {
					-- 预览窗口布局
					-- "vertical"   : 预览在右侧/下方
					-- "horizontal" : 预览在上方/下方
					-- "flex"       : 自动选择
					layout = 'vertical',

					-- 滚动条样式
					-- "float"  : 浮动滚动条
					-- "border" : 边框式滚动条
					-- false    : 无滚动条
					scrollbar = 'float',
				},

				-- 是否全屏显示
				fullscreen = true,

				-- 垂直布局时，主窗口位置和大小
				-- "down:45%" : 主窗口在下方，占 45% 高度
				-- "up:45%"   : 主窗口在上方
				vertical = 'down:45%',

				-- 水平布局时，主窗口位置和大小
				-- "right:60%" : 主窗口在右侧，占 60% 宽度
				-- "left:60%"  : 主窗口在左侧
				horizontal = 'right:60%',

				-- 是否隐藏搜索窗口
				-- "nohidden" : 不隐藏
				-- "hidden"   : 隐藏
				hidden = 'nohidden',
			},

			-- ================================================================
			-- 快捷键映射
			-- ================================================================
			keymap = {
				-- ------------------------------------------------------------
				-- Neovim 内置操作的快捷键
				-- 这些快捷键在 Neovim 的浮动窗口中生效
				-- ------------------------------------------------------------
				builtin = {
					-- Ctrl+f    : 切换全屏
					["<c-f>"] = "toggle-fullscreen",

					-- Ctrl+r    : 切换预览窗口的自动换行
					["<c-r>"] = "toggle-preview-wrap",

					-- Ctrl+p    : 切换预览窗口显示/隐藏
					["<c-p>"] = "toggle-preview",

					-- Ctrl+y    : 预览窗口向下翻页
					["<c-y>"] = "preview-page-down",

					-- Ctrl+l    : 预览窗口向上翻页
					["<c-l>"] = "preview-page-up",

					-- Shift+左箭头 : 重置预览窗口位置
					["<S-left>"] = "preview-page-reset",
				},

				-- ------------------------------------------------------------
				-- fzf 进程的快捷键
				-- 这些快捷键由 fzf 程序处理
				-- ------------------------------------------------------------
				fzf = {
					-- Esc       : 取消/关闭
					["esc"] = "abort",

					-- Ctrl+h    : 删除光标左边所有内容（清空输入）
					["ctrl-h"] = "unix-line-discard",

					-- Ctrl+k    : 结果列表向下翻半页
					["ctrl-k"] = "half-page-down",

					-- Ctrl+b    : 结果列表向上翻半页
					["ctrl-b"] = "half-page-up",

					-- Ctrl+n    : 光标移动到行首
					["ctrl-n"] = "beginning-of-line",

					-- Ctrl+a    : 光标移动到行尾
					["ctrl-a"] = "end-of-line",

					-- Alt+a     : 全选/取消全选
					["alt-a"] = "toggle-all",

					-- F3        : 切换预览窗口自动换行
					["f3"] = "toggle-preview-wrap",

					-- F4        : 切换预览窗口显示/隐藏
					["f4"] = "toggle-preview",

					-- Shift+下箭头 : 预览窗口向下翻页
					["shift-down"] = "preview-page-down",

					-- Shift+上箭头 : 预览窗口向上翻页
					["shift-up"] = "preview-page-up",

					-- Ctrl+e    : 选择下一项（向下移动）
					["ctrl-e"] = "down",

					-- Ctrl+u    : 选择上一项（向上移动）
					["ctrl-u"] = "up",
				},
			},

			-- ================================================================
			-- 预览器配置
			-- ================================================================
			previewers = {
				-- ------------------------------------------------------------
				-- head 预览器（显示文件开头）
				-- ------------------------------------------------------------
				head = {
					cmd = "head",    -- 使用 head 命令
					args = nil,      -- 命令参数（nil 使用默认）
				},

				-- ------------------------------------------------------------
				-- Git diff 预览器
				-- ------------------------------------------------------------
				git_diff = {
					-- 已删除文件的 diff 命令
					cmd_deleted = "git diff --color HEAD --",

					-- 已修改文件的 diff 命令
					cmd_modified = "git diff --color HEAD",

					-- 未跟踪文件的 diff 命令
					cmd_untracked = "git diff --color --no-index /dev/null",

					-- 如果安装了 delta，可以启用更好看的 diff 显示
					-- pager = "delta",
				},

				-- ------------------------------------------------------------
				-- man 手册预览器
				-- ------------------------------------------------------------
				man = {
					-- 显示 man 手册的命令
					-- col -bx: 移除退格字符，使输出更干净
					cmd = "man -c %s | col -bx",
				},

				-- ------------------------------------------------------------
				-- 内置预览器配置
				-- ------------------------------------------------------------
				builtin = {
					-- 是否启用语法高亮
					syntax = true,

					-- 语法高亮的行数限制（0 = 无限制）
					-- 超过此行数的文件不进行语法高亮（提高性能）
					syntax_limit_l = 0,

					-- 语法高亮的字节限制（0 = 无限制）
					-- 1024 * 1024 = 1MB
					syntax_limit_b = 1024 * 1024,

					-- 是否启用跳转到指定行的功能
					-- 搜索结果中包含行号时，预览会自动定位到该行
					jump_to_line = true,

					-- 是否显示预览窗口标题
					title = false,
				},
			},

			-- ================================================================
			-- 文件搜索配置
			-- ================================================================
			files = {
				-- 搜索提示符
				prompt = 'Files❯ ',

				-- 是否在单独的进程中运行搜索命令
				-- true: 提高性能，避免阻塞 Neovim
				multiprocess = true,

				-- 是否显示 Git 状态图标
				-- 显示文件的 Git 状态（已修改、已暂存等）
				git_icons = true,

				-- 是否显示文件类型图标
				file_icons = true,

				-- 是否给图标着色
				color_icons = true,

				-- 搜索命令优先级：cmd > fd > rg > find
				-- 可以取消注释下面这行来自定义搜索命令
				-- cmd = "find . -type f -printf '%P\n'",

				-- find 命令的选项
				-- 排除 .git 目录
				find_opts = [[-type f -not -path '*/\.git/*' -printf '%P\n']],

				-- ripgrep (rg) 的选项
				-- --hidden: 包含隐藏文件
				-- --follow: 跟随符号链接
				-- -g '!.git': 排除 .git 目录
				rg_opts = "--color=never --files --hidden --follow -g '!.git'",

				-- fd 命令的选项
				-- --hidden: 包含隐藏文件
				-- --follow: 跟随符号链接
				-- --exclude .git: 排除 .git 目录
				fd_opts = "--color=never --type f --hidden --follow --exclude .git",
			},

			-- ================================================================
			-- Buffer 搜索配置
			-- ================================================================
			buffers = {
				-- 搜索提示符
				prompt = 'Buffers❯ ',

				-- 是否显示文件类型图标
				file_icons = true,

				-- 是否给图标着色
				color_icons = true,

				-- 按最近使用时间排序
				-- true: 最近使用的 buffer 排在最前面
				sort_lastused = true,
			},

			-- ================================================================
			-- 全局文本搜索配置（grep）
			-- ================================================================
			grep = {
				-- ripgrep 的选项
				-- --color=always     : 输出带颜色（用于高亮匹配）
				-- --line-number      : 显示行号
				-- --column           : 显示列号
				-- --smart-case       : 智能大小写
				-- --ignore-file      : 使用 .fzfignore 文件排除目录/文件
				rg_opts = "--color=always --line-number --column --smart-case --ignore-file=.fzfignore",

				-- 使用内置预览器
				previewer = "builtin",

				-- 允许跳转到匹配的行
				jump_to_line = true,
			},
		})
	end
}

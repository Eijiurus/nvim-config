-- ============================================================================
-- Typst 代码片段
-- 文件位置：~/.config/nvim/snippets/typst.lua
-- 使用 LuaSnip 插件
-- ============================================================================
--
-- 使用方法：
-- 1. 输入片段触发词（如 "sum"）
-- 2. 按 Tab 展开片段
-- 3. 按 Tab 跳转到下一个占位符
-- 4. 按 Shift+Tab 跳转到上一个占位符
--
-- ============================================================================

local ls = require("luasnip")
local s = ls.snippet           -- 创建片段
local t = ls.text_node         -- 纯文本节点
local i = ls.insert_node       -- 可编辑占位符
local f = ls.function_node     -- 函数节点（动态内容）
local c = ls.choice_node       -- 选择节点
local d = ls.dynamic_node      -- 动态节点
local r = ls.restore_node      -- 恢复节点
local fmt = require("luasnip.extras.fmt").fmt  -- 格式化模板

-- ============================================================================
-- 数学片段
-- ============================================================================
local math_snippets = {
	-- ========================================================================
	-- 分数
	-- 触发词：// 或 frac
	-- 输出：(num)/(den)
	-- ========================================================================
	s("//", fmt("({})/({})", { i(1, "num"), i(2, "den") })),
	s("frac", fmt("({})/({})", { i(1, "num"), i(2, "den") })),

	-- ========================================================================
	-- 求和符号
	-- 触发词：sum
	-- 输出：sum_(i=1)^(n)
	-- ========================================================================
	s("sum", fmt("sum_({})^({})", { i(1, "i=1"), i(2, "n") })),

	-- ========================================================================
	-- 积分符号
	-- 触发词：int
	-- 输出：integral_(a)^(b) ... dif x
	-- ========================================================================
	s("int", fmt("integral_({})^({}) {} dif {}", {
		i(1, "a"),
		i(2, "b"),
		i(3, "f(x)"),
		i(4, "x"),
	})),

	-- ========================================================================
	-- 极限
	-- 触发词：lim
	-- 输出：lim_(x -> a)
	-- ========================================================================
	s("lim", fmt("lim_({} -> {})", { i(1, "x"), i(2, "oo") })),

	-- ========================================================================
	-- 上标
	-- 触发词：^
	-- 输出：^(exp)
	-- ========================================================================
	s("^", fmt("^({})", { i(1) })),

	-- ========================================================================
	-- 下标
	-- 触发词：_
	-- 输出：_(sub)
	-- ========================================================================
	s("_", fmt("_({})", { i(1) })),

	-- ========================================================================
	-- 平方根
	-- 触发词：sqrt
	-- 输出：sqrt(x)
	-- ========================================================================
	s("sqrt", fmt("sqrt({})", { i(1, "x") })),

	-- ========================================================================
	-- n次方根
	-- 触发词：nrt
	-- 输出：root(n, x)
	-- ========================================================================
	s("nrt", fmt("root({}, {})", { i(1, "n"), i(2, "x") })),

	-- ========================================================================
	-- 向量（粗体）
	-- 触发词：vec
	-- 输出：bold(v)
	-- ========================================================================
	s("vec", fmt("bold({})", { i(1, "v") })),

	-- ========================================================================
	-- 矩阵
	-- 触发词：mat
	-- 输出：mat(a, b; c, d)
	-- ========================================================================
	s("mat", fmt("mat({}, {}; {}, {})", {
		i(1, "a"), i(2, "b"),
		i(3, "c"), i(4, "d"),
	})),

	-- ========================================================================
	-- 行列式
	-- 触发词：det
	-- 输出：mat(delim: "|", ...)
	-- ========================================================================
	s("det", fmt('mat(delim: "|", {}, {}; {}, {})', {
		i(1, "a"), i(2, "b"),
		i(3, "c"), i(4, "d"),
	})),

	-- ========================================================================
	-- 偏导数
	-- 触发词：pdv
	-- 输出：(diff f)/(diff x)
	-- ========================================================================
	s("pdv", fmt("(diff {})/(diff {})", { i(1, "f"), i(2, "x") })),

	-- ========================================================================
	-- 常用集合
	-- ========================================================================
	s("RR", t("RR")),      -- 实数集 ℝ
	s("NN", t("NN")),      -- 自然数集 ℕ
	s("ZZ", t("ZZ")),      -- 整数集 ℤ
	s("QQ", t("QQ")),      -- 有理数集 ℚ
	s("CC", t("CC")),      -- 复数集 ℂ

	-- ========================================================================
	-- 希腊字母快捷方式
	-- ========================================================================
	s(";a", t("alpha")),
	s(";b", t("beta")),
	s(";g", t("gamma")),
	s(";d", t("delta")),
	s(";e", t("epsilon")),
	s(";z", t("zeta")),
	s(";t", t("theta")),
	s(";l", t("lambda")),
	s(";m", t("mu")),
	s(";p", t("pi")),
	s(";s", t("sigma")),
	s(";f", t("phi")),
	s(";o", t("omega")),
}

-- ============================================================================
-- 文档结构片段
-- ============================================================================
local structure_snippets = {
	-- ========================================================================
	-- 图片
	-- 触发词：fig
	-- ========================================================================
	s("fig", fmt([[
#figure(
  image("{}"),
  caption: [{}],
) <{}>
]], { i(1, "path/to/image.svg"), i(2, "Caption"), i(3, "label") })),

	-- ========================================================================
	-- 代码块
	-- 触发词：code
	-- ========================================================================
	s("code", fmt([[
```{}
{}
```
]], { i(1, "python"), i(2, "code") })),

	-- ========================================================================
	-- 引用块（Blockquote）
	-- 触发词：quote
	-- ========================================================================
	s("quote", fmt([[
#quote(block: true, attribution: [{}])[
  {}
]
]], { i(1, "Author"), i(2, "Quote content") })),

	-- ========================================================================
	-- 定理环境
	-- 触发词：thm
	-- ========================================================================
	s("thm", fmt([[
#block(
  fill: luma(230),
  inset: 8pt,
  radius: 4pt,
  width: 100%,
)[
  *定理 {}* ({})

  {}
]
]], { i(1, "1"), i(2, "Name"), i(3, "Content") })),

	-- ========================================================================
	-- 定义环境
	-- 触发词：def
	-- ========================================================================
	s("def", fmt([[
#block(
  fill: rgb("#e8f4f8"),
  inset: 8pt,
  radius: 4pt,
  width: 100%,
)[
  *定义 {}* ({})

  {}
]
]], { i(1, "1"), i(2, "Name"), i(3, "Content") })),

	-- ========================================================================
	-- 例子环境
	-- 触发词：ex
	-- ========================================================================
	s("ex", fmt([[
#block(
  stroke: (left: 2pt + blue),
  inset: (left: 12pt, y: 8pt),
)[
  *例 {}*

  {}
]
]], { i(1, "1"), i(2, "Content") })),

	-- ========================================================================
	-- 链接
	-- 触发词：link
	-- ========================================================================
	s("link", fmt('#link("{}")[{}]', { i(1, "url"), i(2, "text") })),

	-- ========================================================================
	-- 表格
	-- 触发词：table
	-- ========================================================================
	s("table", fmt([[
#figure(
  table(
    columns: {},
    inset: 10pt,
    align: horizon,
    table.header(
      [{}], [{}],
    ),
    [{}], [{}],
  ),
  caption: [{}],
)
]], {
		i(1, "2"),
		i(2, "Header 1"), i(3, "Header 2"),
		i(4, "Cell 1"), i(5, "Cell 2"),
		i(6, "Table caption"),
	})),
}

-- ============================================================================
-- 实用片段
-- ============================================================================
local utility_snippets = {
	-- ========================================================================
	-- 今天的日期
	-- 触发词：today
	-- ========================================================================
	s("today", f(function()
		return os.date("%Y-%m-%d")
	end)),

	-- ========================================================================
	-- 当前时间
	-- 触发词：now
	-- ========================================================================
	s("now", f(function()
		return os.date("%Y-%m-%d %H:%M")
	end)),

	-- ========================================================================
	-- 注释块
	-- 触发词：//=
	-- ========================================================================
	s("//=", fmt([[
// ============================================================================
// {}
// ============================================================================
]], { i(1, "Section") })),

	-- ========================================================================
	-- 文档模板
	-- 触发词：template
	-- ========================================================================
	s("template", fmt([[
#set page(paper: "a4", margin: 2cm)
#set text(font: "New Computer Modern", size: 11pt, lang: "zh")
#set heading(numbering: "1.1")
#set par(justify: true, leading: 0.8em)

#align(center)[
  #text(size: 20pt, weight: "bold")[{}]

  {}

  #datetime.today().display()
]

#outline()

= {}

{}
]], {
		i(1, "Document Title"),
		i(2, "Author"),
		i(3, "Introduction"),
		i(4, "Content"),
	})),
}

-- ============================================================================
-- 返回所有片段
-- ============================================================================
-- 合并所有片段表
local all_snippets = {}
for _, snippet in ipairs(math_snippets) do
	table.insert(all_snippets, snippet)
end
for _, snippet in ipairs(structure_snippets) do
	table.insert(all_snippets, snippet)
end
for _, snippet in ipairs(utility_snippets) do
	table.insert(all_snippets, snippet)
end

return all_snippets
